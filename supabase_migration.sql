-- ============================================================
-- SECTION 19: VERIFICATION QUERY
-- Run this to confirm all 16 tables exist and have correct columns.
-- Expected output: 16 rows, each with a column_count > 0.
-- ============================================================
SELECT
  t.table_name,
  COUNT(c.column_name)                                   AS column_count,
  CASE t.table_type WHEN 'BASE TABLE' THEN 'TABLE' ELSE t.table_type END AS type
FROM information_schema.tables t
JOIN information_schema.columns c
  ON  c.table_schema = t.table_schema
  AND c.table_name   = t.table_name
WHERE t.table_schema = 'public'
  AND t.table_name IN (
    'profiles', 'membership_plans', 'trainers', 'members',
    'payments', 'attendance', 'settings', 'notifications',
    'activities', 'whatsapp_config', 'reminder_logs',
    'workouts', 'diet_plans', 'expenses', 'leads', 'visitors'
  )
GROUP BY t.table_name, t.table_type
ORDER BY t.table_name;
