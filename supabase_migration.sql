-- Automatically updates `updated_at` to NOW() on any row update.
CREATE OR REPLACE FUNCTION public.handle_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

COMMENT ON FUNCTION public.handle_updated_at IS
  'Generic trigger function that sets updated_at = NOW() before any UPDATE.';


-- ============================================================
-- SECTION 1: PROFILES
-- Extends auth.users with gym-specific staff/admin data.
-- Passwords are NEVER stored here — Supabase Auth manages them.
-- ============================================================
CREATE TABLE IF NOT EXISTS public.profiles (
  -- Links 1:1 to auth.users. Cascade delete removes profile when auth user is deleted.
  id             UUID       PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  display_name   TEXT       NOT NULL,
  role           TEXT       NOT NULL DEFAULT 'staff'
                              CHECK (role IN ('super_admin', 'admin', 'manager', 'staff', 'trainer')),
  avatar_url     TEXT,
  phone          TEXT,
  is_active      BOOLEAN    NOT NULL DEFAULT TRUE,
  -- Audit
  created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
  -- NOTE: No deleted_at here. Disable users via Supabase Auth dashboard instead.
);

COMMENT ON TABLE public.profiles IS
  'Gym staff and admin profiles. Linked 1:1 to auth.users via UUID.
   Role controls UI access. Passwords are managed by Supabase Auth.
   PRODUCTION: Add gym_id for multi-tenant isolation.';

COMMENT ON COLUMN public.profiles.role IS
  'super_admin: full access | admin: manage members/payments |
   manager: reports + renewals | staff: attendance + check-in | trainer: own clients only';

CREATE INDEX IF NOT EXISTS idx_profiles_role     ON public.profiles(role);
CREATE INDEX IF NOT EXISTS idx_profiles_active   ON public.profiles(is_active) WHERE is_active = TRUE;

DROP TRIGGER IF EXISTS trg_profiles_updated_at ON public.profiles;
CREATE TRIGGER trg_profiles_updated_at
  BEFORE UPDATE ON public.profiles
  FOR EACH ROW EXECUTE FUNCTION public.handle_updated_at();

-- Auto-create a profile row whenever a new Supabase Auth user is registered.
CREATE OR REPLACE FUNCTION public.handle_new_auth_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = ''
AS $$
BEGIN
  INSERT INTO public.profiles (id, display_name, role)
  VALUES (
    NEW.id,
    COALESCE(
      NEW.raw_user_meta_data ->> 'display_name',
      split_part(NEW.email, '@', 1)
    ),
    COALESCE(NEW.raw_user_meta_data ->> 'role', 'staff')
  )
  ON CONFLICT (id) DO NOTHING;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_on_auth_user_created ON auth.users;
CREATE TRIGGER trg_on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_auth_user();


