-- ============================================================
-- SECTION 17: ROW LEVEL SECURITY (RLS)
-- ============================================================
-- DEVELOPMENT POLICY (current):
--   Authenticated users can SELECT and mutate all rows.
--   Unauthenticated (anon) users have ZERO access.
--
-- ⚠️  PRODUCTION UPGRADE CHECKLIST:
--   □ 1. Add `gym_id UUID` to every table for multi-tenant isolation.
--   □ 2. Set `gym_id = (SELECT gym_id FROM profiles WHERE id = auth.uid())`
--        in all RLS policies.
--   □ 3. Restrict DELETE/UPDATE to admin roles:
--          USING (EXISTS(SELECT 1 FROM profiles WHERE id = auth.uid()
--                        AND role IN ('super_admin','admin')))
--   □ 4. Move api_key / Twilio credentials to Supabase Vault.
--   □ 5. Implement a separate audit_log table triggered on sensitive mutations.
--   □ 6. Use Supabase Edge Functions for all messaging (never expose Twilio from client).
--   □ 7. Rate-limit anon requests via Supabase middleware.
--   □ 8. Enable email confirmation for new staff accounts.
-- ============================================================

DO $$
DECLARE
  tbl TEXT;
BEGIN
  FOR tbl IN SELECT UNNEST(ARRAY[
    'profiles', 'membership_plans', 'trainers', 'members',
    'payments', 'attendance', 'settings', 'notifications',
    'activities', 'whatsapp_config', 'reminder_logs',
    'workouts', 'diet_plans', 'expenses', 'leads', 'visitors'
  ]) LOOP
    -- Enable RLS
    EXECUTE FORMAT('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY;', tbl);

    -- Remove old open-access policies from v1 if they exist
    EXECUTE FORMAT(
      'DROP POLICY IF EXISTS "Enable all access for anon" ON public.%I;', tbl
    );

    -- DEVELOPMENT: authenticated users can read anything
    EXECUTE FORMAT(
      'DROP POLICY IF EXISTS "dev_select_authenticated" ON public.%I;
       CREATE POLICY "dev_select_authenticated" ON public.%I
         FOR SELECT TO authenticated USING (true);',
      tbl, tbl
    );

    -- DEVELOPMENT: authenticated users can mutate anything
    EXECUTE FORMAT(
      'DROP POLICY IF EXISTS "dev_all_authenticated" ON public.%I;
       CREATE POLICY "dev_all_authenticated" ON public.%I
         FOR ALL TO authenticated USING (true) WITH CHECK (true);',
      tbl, tbl
    );
  END LOOP;
END $$;


