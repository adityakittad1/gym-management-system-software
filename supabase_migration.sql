  BEFORE UPDATE ON public.payments
  FOR EACH ROW EXECUTE FUNCTION public.handle_updated_at();

-- VIEW: payments_view — provides member_name via JOIN for UI display
CREATE OR REPLACE VIEW public.payments_view AS
SELECT
  p.id,
  p.member_id,
  m.name          AS member_name,
  m.phone         AS member_phone,
  p.amount,
  p.plan,
  p.date,
  p.method,
  p.status,
  p.notes,
  p.created_at,
  p.updated_at,
  p.created_by
FROM public.payments p
JOIN public.members m ON m.id = p.member_id
WHERE p.deleted_at IS NULL;

COMMENT ON VIEW public.payments_view IS
  'Payments joined with member name/phone for display.
   member_name is derived via JOIN — never stored redundantly.';


-- ============================================================
-- SECTION 6: ATTENDANCE  (3NF — member_name NOT stored)
-- One row per member per day enforced by UNIQUE constraint.
-- ============================================================
CREATE TABLE IF NOT EXISTS public.attendance (
  id              BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  -- FK only — member_name comes from JOIN with members_view
  member_id       BIGINT      NOT NULL REFERENCES public.members(id) ON DELETE CASCADE,
  date            DATE        NOT NULL DEFAULT CURRENT_DATE,
  check_in_time   TEXT,      -- stored as text e.g. "10:30 AM" for UI compatibility
  check_out_time  TEXT,
  status          TEXT        NOT NULL DEFAULT 'present'
                    CHECK (status IN ('present', 'absent', 'late')),
  notes           TEXT,
  -- Audit
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  -- Enforce: one attendance record per member per calendar day
  CONSTRAINT uq_attendance_member_date UNIQUE (member_id, date)
);

COMMENT ON TABLE public.attendance IS
  '3NF: member_name is NOT stored. Join with members_view to get it.
   UNIQUE(member_id, date) prevents duplicate check-ins on the same day.
   Use attendance_view for all display queries.
   No soft delete: attendance records are factual; corrections go via UPDATE.';

CREATE INDEX IF NOT EXISTS idx_attendance_member_id ON public.attendance(member_id);
CREATE INDEX IF NOT EXISTS idx_attendance_date       ON public.attendance(date);

DROP TRIGGER IF EXISTS trg_attendance_updated_at ON public.attendance;
CREATE TRIGGER trg_attendance_updated_at
  BEFORE UPDATE ON public.attendance
  FOR EACH ROW EXECUTE FUNCTION public.handle_updated_at();

-- VIEW: attendance_view — provides member info via JOIN
CREATE OR REPLACE VIEW public.attendance_view AS
SELECT
  a.id,
  a.member_id,
  m.name          AS member_name,
  m.phone         AS member_phone,
  a.date,
  a.check_in_time,
  a.check_out_time,
  a.status,
  a.notes,
  a.created_at,
  a.updated_at
FROM public.attendance a
JOIN public.members m ON m.id = a.member_id
WHERE m.deleted_at IS NULL;

COMMENT ON VIEW public.attendance_view IS
  'Attendance with member name/phone derived via JOIN.
   Excludes attendance for soft-deleted members.';


