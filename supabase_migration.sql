-- ──────────────────────────────────────────────────────────────
-- VIEW: members_view  — use this for ALL SELECT queries in the app
-- ──────────────────────────────────────────────────────────────
CREATE OR REPLACE VIEW public.members_view AS
SELECT
  m.id,
  m.name,
  m.phone,
  m.email,
  m.plan,
  m.plan_id,
  m.join_date,
  m.expiry_date,
  -- days_remaining: computed live from today's date, minimum 0
  GREATEST(0, (m.expiry_date - CURRENT_DATE)::INTEGER)        AS days_remaining,
  -- status: derived from expiry_date vs CURRENT_DATE
  CASE
    WHEN m.expiry_date < CURRENT_DATE               THEN 'expired'
    WHEN m.expiry_date <= (CURRENT_DATE + 5)        THEN 'expiring'
    ELSE                                                 'active'
  END                                                          AS status,
  m.trainer_id,
  m.weight,
  m.target_weight,
  m.height,
  m.body_fat,
  m.before_image,
  m.after_image,
  m.last_visit,
  m.notes,
  m.created_at,
  m.updated_at,
  m.created_by,
  m.updated_by
FROM public.members m
WHERE m.deleted_at IS NULL;

COMMENT ON VIEW public.members_view IS
  'Active-only members with computed days_remaining and status.
   All application SELECT queries MUST use this view, not the base table.
   INSERT / UPDATE / DELETE still target the base members table.';


-- ============================================================
-- SECTION 5: PAYMENTS  (3NF — member_name NOT stored)
-- ============================================================
CREATE TABLE IF NOT EXISTS public.payments (
  id          BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  -- FK only — member_name comes from JOIN with members_view
  member_id   BIGINT      NOT NULL REFERENCES public.members(id) ON DELETE RESTRICT,
  amount      NUMERIC(10, 2) NOT NULL CHECK (amount > 0),
  plan        TEXT        NOT NULL,
  date        DATE        NOT NULL DEFAULT CURRENT_DATE,
  method      TEXT        NOT NULL DEFAULT 'Cash'
                CHECK (method IN ('Cash', 'UPI', 'Card', 'Bank Transfer', 'Other')),
  status      TEXT        NOT NULL DEFAULT 'paid'
                CHECK (status IN ('paid', 'pending', 'refunded', 'failed')),
  notes       TEXT,
  -- Soft delete: keep audit trail even after correction
  deleted_at  TIMESTAMPTZ,
  -- Audit
  created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_by  UUID        REFERENCES public.profiles(id) ON DELETE SET NULL,
  updated_by  UUID        REFERENCES public.profiles(id) ON DELETE SET NULL
);

COMMENT ON TABLE public.payments IS
  '3NF: member_name is NOT stored. Always JOIN with members_view to get name.
   Use payments_view for display-layer queries.
   Soft-deleted via deleted_at; refunded/failed payments are status-updated, not deleted.';

CREATE INDEX IF NOT EXISTS idx_payments_member_id ON public.payments(member_id);
CREATE INDEX IF NOT EXISTS idx_payments_date      ON public.payments(date)   WHERE deleted_at IS NULL;
CREATE INDEX IF NOT EXISTS idx_payments_status    ON public.payments(status) WHERE deleted_at IS NULL;

DROP TRIGGER IF EXISTS trg_payments_updated_at ON public.payments;
