-- Message Queue
CREATE TABLE IF NOT EXISTS public.message_queue (
  id            BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  member_id     BIGINT      REFERENCES public.members(id) ON DELETE CASCADE,
  phone         TEXT        NOT NULL,
  message       TEXT        NOT NULL,
  template_id   BIGINT      REFERENCES public.whatsapp_templates(id) ON DELETE SET NULL,
  rule_id       BIGINT      REFERENCES public.automation_rules(id) ON DELETE SET NULL,
  status        TEXT        NOT NULL DEFAULT 'pending'
                  CHECK (status IN ('pending','processing','sent','failed','cancelled')),
  scheduled_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  processed_at  TIMESTAMPTZ,
  error_message TEXT,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_message_queue_status      ON public.message_queue(status);
