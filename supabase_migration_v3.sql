-- SECTION E: REBUILD members_view WITH NEW FIELDS
-- ============================================================
CREATE OR REPLACE VIEW public.members_view AS
SELECT
  m.id,
  m.name,
  m.phone,
  m.email,
  m.plan,
  m.plan_id,
  m.join_date,
  m.expiry_date,
  GREATEST(0, (m.expiry_date - CURRENT_DATE)::INTEGER)   AS days_remaining,
  CASE
    WHEN m.expiry_date < CURRENT_DATE               THEN 'expired'
    WHEN m.expiry_date <= (CURRENT_DATE + 5)        THEN 'expiring'
    ELSE                                                 'active'
  END                                                     AS status,
  m.trainer_id,
  m.weight,
  m.target_weight,
  m.height,
  m.body_fat,
  m.before_image,
  m.after_image,
  m.last_visit,
  m.notes,
  -- New enterprise fields
  m.gender,
  m.date_of_birth,
  m.address,
  m.emergency_contact,
  m.emergency_contact_name,
  m.blood_group,
  m.medical_conditions,
  m.profile_photo,
  m.membership_status,
  m.renewal_count,
  m.is_active,
  m.created_at,
  m.updated_at,
  m.created_by,
  m.updated_by
FROM public.members m
WHERE m.deleted_at IS NULL;

