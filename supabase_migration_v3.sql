CREATE TABLE IF NOT EXISTS public.measurements (
  id          BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  member_id   BIGINT      NOT NULL REFERENCES public.members(id) ON DELETE CASCADE,
  date        DATE        NOT NULL DEFAULT CURRENT_DATE,
  weight      NUMERIC(5,2),
  body_fat    NUMERIC(5,2),
  chest       NUMERIC(5,2),
  waist       NUMERIC(5,2),
  hips        NUMERIC(5,2),
  arms        NUMERIC(5,2),
  thighs      NUMERIC(5,2),
  notes       TEXT,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_by  UUID        REFERENCES public.profiles(id) ON DELETE SET NULL,
  CONSTRAINT uq_measurements_member_date UNIQUE (member_id, date)
);

COMMENT ON TABLE public.measurements IS
  'Body measurement history per member. One row per member per date.';

CREATE INDEX IF NOT EXISTS idx_measurements_member_id ON public.measurements(member_id);
CREATE INDEX IF NOT EXISTS idx_measurements_date      ON public.measurements(date);

ALTER TABLE public.measurements ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "auth_full_access_measurements" ON public.measurements;
CREATE POLICY "auth_full_access_measurements"
  ON public.measurements FOR ALL
  USING (auth.role() = 'authenticated')
  WITH CHECK (auth.role() = 'authenticated');

