DECLARE tbl TEXT;
BEGIN
  FOR tbl IN SELECT UNNEST(ARRAY[
    'measurements','whatsapp_sessions','whatsapp_templates',
    'automation_rules','message_queue','delivery_history','activity_logs'
  ]) LOOP
    EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY', tbl);
    EXECUTE format('DROP POLICY IF EXISTS "auth_full_access_%1$s" ON public.%1$I', tbl);
    EXECUTE format(
      'CREATE POLICY "auth_full_access_%1$s" ON public.%1$I FOR ALL
       USING (auth.role() = ''authenticated'')
       WITH CHECK (auth.role() = ''authenticated'')',
      tbl
    );
  END LOOP;
END $$;

